package cat.copernic.easytraza_backend.model.enums;

/**
 * Estats possibles d'una comanda del projecte EasyTraza.
 */
public enum EstatComanda {
    /**
     * Valor `PENDENT` utilitzat per representar l'estat pendent.
     */
    PENDENT,
    /**
     * Valor `PREPARADA` utilitzat per representar l'estat preparada.
     */
    PREPARADA,
    /**
     * Valor `ALBARA_GENERAT` utilitzat per representar l'estat albara generat.
     */
    ALBARA_GENERAT,
    /**
     * Valor `CANCELADA` utilitzat per representar l'estat  CANCELADA.
     */
    CANCELADA
}
